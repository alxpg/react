//Las variables con let se puede reasignar el valor y se pueden inicializar sin valor
//Las variables con let son globales
//Las variables con const son por bloque
//Las variables con const no se pueden reasignar el valor y no se pueden inicializar sin valor
let contacto = "Cadena de string";
contacto;
console.log(contacto); // 2000

let precio;
precio = 3000;
console.log(precio); // 3000